package lec77_java_coding_challenge;

/*
Q9: What will be the result of executing the following code: 
*/

// The values are coming from ASCII Table
public class J07_playWithVariable {

	public static void main(String[] args) {
		System.out.println('j');
		System.out.println('a');
		System.out.println('j'+'a'); // The value of j in ASCII Table is 21, a = 182, v = 203
		System.out.println('v');
		System.out.println('j'+'v');
		System.out.println('j' + 'a' + 'v' + 'a');
	}

}
