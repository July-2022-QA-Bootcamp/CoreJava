package lec14_java_conditional_statements;

public class G_example_of_switch_in_finding_vowel {
	public static void main(String[] args) {
		char ch = 'n';

		switch (ch) {
		
		case 'a':
			System.out.println("Vowel");
			break;
		
		case 'e':
			System.out.println("Vowel");
			break;
		
		case 'i':
			System.out.println("Vowel");
			break;
		
		case 'o':
			System.out.println("Vowel");
			break;
		
		case 'u':
			System.out.println("Vowel");
			break;
		
		case 'A':
			System.out.println("Vowel");
			break;
		
		case 'E':
			System.out.println("Vowel");
			break;
		
		case 'I':
			System.out.println("Vowel");
			break;
		
		case 'O':
			System.out.println("Vowel");
			break;
		
		case 'U':
			System.out.println("Vowel");
			break;
		
		default:
			System.out.println("Consonant");
		}
	}
}
