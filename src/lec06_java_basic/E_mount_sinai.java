package lec06_java_basic;

// use of \n --> creating new line  ---> a lot of use of \n in IT industry
// use of \t --> creating tab  ---> a lot of use of \t
// Below 2 is: extra information, not used generally
// use of \b --> creating back space -- not used --- also doesn't work in eclipse (Intellij ok)
// use of \r --> creating carriage return -- not used -- similar like \n

public class E_mount_sinai {
	public static void main(String[] args) {
		// A regular String
		System.out.println("The Mount Sinai Health System is a hospital network in New York City. It was formed in September 2013 by merging the operations of Continuum Health Partners and the Mount Sinai Medical Center.");
		
		// we are going to use \n, \t, "1 tab = 8 space"
		System.out.println("\n---------------------------------------------------------------------------------------\n");
		System.out.println("\tThe Mount Sinai Health System is a hospital network in New York City. \n\tIt was formed in September 2013 by merging the operations of Continuum Health Partners and the Mount Sinai Medical Center.");
	
	
	}
	
	

}
